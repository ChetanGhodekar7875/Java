package Looping;

public class Demo12 {
    public static void main(String[] args) {
        int num=5;
        while (num>5){
            num--;
        }
        System.out.println("NUMBER VALUE IS "+num);
        int num2=5;
        do{
            num2--;
        }while (num2>5);

        System.out.println("NUMBER VALUE IS "+num2);
    }
}
