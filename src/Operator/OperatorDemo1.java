package Operator;

public class OperatorDemo1 {
    public static void main(String[] args) {
        int a=10;
        int b=10;
        System.out.println(++a);//PRE-INCREMENT->11
        System.out.println(b++);//POST-INCREMENT ->10

        int x=10;
        int y=10;
        System.out.println(--x);//PRE-DECREMENT->9
        System.out.println(y--);//POST-DECREMENT->10
    }
}
